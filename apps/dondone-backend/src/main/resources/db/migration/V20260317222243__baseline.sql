CREATE TABLE wage_deposits (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES users (id),
    year_month VARCHAR(7) NOT NULL,
    deposit_date DATE NOT NULL,
    actual_deposit_amount BIGINT NOT NULL,
    deductions_known BOOLEAN NOT NULL,
    note VARCHAR(500),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT ck_wage_deposits_year_month CHECK (
        year_month ~ '^\d{4}-(0[1-9]|1[0-2])$'
    ),
    CONSTRAINT ck_wage_deposits_deposit_date_month CHECK (
        to_char(deposit_date, 'YYYY-MM') = year_month
