CREATE INDEX idx_wage_deposits_user_year_month_deposit_date
    ON wage_deposits (user_id, year_month, deposit_date DESC, created_at DESC, id DESC);

CREATE TABLE wage_verifications (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES users (id),
    year_month VARCHAR(7) NOT NULL,
    workplace_id BIGINT NOT NULL REFERENCES workplaces (id),
    overtime_minutes BIGINT NOT NULL,
    night_minutes BIGINT NOT NULL,
    modified_record_count INTEGER NOT NULL,
    actual_deposit_amount BIGINT NOT NULL,
    deductions_known BOOLEAN NOT NULL,
    memo VARCHAR(500),
    status VARCHAR(30) NOT NULL,
    resolution_stage VARCHAR(50) NOT NULL,
    base_estimate BIGINT NOT NULL,
    overtime_premium BIGINT NOT NULL,
    night_premium BIGINT NOT NULL,
    estimated_total BIGINT NOT NULL,
    difference_amount BIGINT NOT NULL,
    difference_rate NUMERIC(10, 4) NOT NULL,
    threshold_absolute_won BIGINT NOT NULL,
    threshold_relative_percent NUMERIC(10, 4) NOT NULL,
    threshold_deduction_relaxed BOOLEAN NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT ck_wage_verifications_year_month CHECK (
        year_month ~ '^\d{4}-(0[1-9]|1[0-2])$'
    ),
    CONSTRAINT ck_wage_verifications_status CHECK (
        status IN ('MATCHED', 'CHECK_REQUIRED')
    ),
    CONSTRAINT ck_wage_verifications_resolution_stage CHECK (
        resolution_stage IN (
            'SELF_CHECK',
            'EMPLOYER_CONFIRMATION_RECOMMENDED',
            'EXTERNAL_HELP_PREPARATION'
        )
    ),
    CONSTRAINT ck_wage_verifications_amounts CHECK (
        actual_deposit_amount >= 0
        AND base_estimate >= 0
        AND overtime_premium >= 0
        AND night_premium >= 0
        AND estimated_total >= 0
        AND threshold_absolute_won >= 0
    ),
    CONSTRAINT ck_wage_verifications_minutes_and_counts CHECK (
        overtime_minutes >= 0
        AND night_minutes >= 0
        AND modified_record_count >= 0
    ),
    CONSTRAINT ck_wage_verifications_difference_rate CHECK (
        difference_rate >= 0
    ),
    CONSTRAINT ck_wage_verifications_threshold_relative_percent CHECK (
        threshold_relative_percent >= 0 AND threshold_relative_percent <= 1
    )
);

CREATE INDEX idx_wage_verifications_user_created_at
    ON wage_verifications (user_id, created_at DESC, id DESC);

CREATE INDEX idx_wage_verifications_user_year_month_created_at
    ON wage_verifications (user_id, year_month, created_at DESC, id DESC);

