CREATE EXTENSION IF NOT EXISTS btree_gist;
CREATE EXTENSION IF NOT EXISTS pgcrypto;

CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$;

CREATE TABLE users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email VARCHAR(255) NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    name VARCHAR(100) NOT NULL,
    role VARCHAR(20) NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uk_users_email UNIQUE (email),
    CONSTRAINT ck_users_role CHECK (role IN ('USER', 'ADMIN'))
);

CREATE TABLE workplaces (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES users (id),
    name VARCHAR(100) NOT NULL,
    address VARCHAR(255) NOT NULL,
    map_label VARCHAR(100),
    latitude DOUBLE PRECISION NOT NULL,
    longitude DOUBLE PRECISION NOT NULL,
    allowed_radius_meters INTEGER,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT ck_workplaces_latitude CHECK (latitude BETWEEN -90 AND 90),
    CONSTRAINT ck_workplaces_longitude CHECK (longitude BETWEEN -180 AND 180),
    CONSTRAINT ck_workplaces_allowed_radius_meters CHECK (
        allowed_radius_meters IS NULL OR allowed_radius_meters > 0
    )
);

CREATE INDEX idx_workplaces_user_created_at
    ON workplaces (user_id, created_at DESC, id DESC);

CREATE TABLE work_contracts (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workplace_id BIGINT NOT NULL REFERENCES workplaces (id),
    pay_unit VARCHAR(20) NOT NULL,
    base_pay_amount NUMERIC(19, 2) NOT NULL,
    daily_work_minutes INTEGER,
    monthly_work_minutes INTEGER,
    normalized_hourly_wage NUMERIC(19, 2) NOT NULL,
    effective_from DATE NOT NULL,
    effective_to DATE,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT ck_work_contracts_pay_unit CHECK (pay_unit IN ('HOURLY', 'DAILY', 'MONTHLY')),
    CONSTRAINT ck_work_contracts_base_pay_amount CHECK (base_pay_amount > 0),
    CONSTRAINT ck_work_contracts_normalized_hourly_wage CHECK (normalized_hourly_wage > 0),
    CONSTRAINT ck_work_contracts_daily_work_minutes CHECK (
        daily_work_minutes IS NULL OR daily_work_minutes > 0
    ),
    CONSTRAINT ck_work_contracts_monthly_work_minutes CHECK (
        monthly_work_minutes IS NULL OR monthly_work_minutes > 0
    ),
    CONSTRAINT ck_work_contracts_unit_payload CHECK (
        (pay_unit = 'HOURLY' AND daily_work_minutes IS NULL AND monthly_work_minutes IS NULL)
        OR (pay_unit = 'DAILY' AND daily_work_minutes IS NOT NULL AND monthly_work_minutes IS NULL)
        OR (pay_unit = 'MONTHLY' AND monthly_work_minutes IS NOT NULL)
    ),
    CONSTRAINT ck_work_contracts_date_range CHECK (
        effective_to IS NULL OR effective_to >= effective_from
    )
);

CREATE UNIQUE INDEX uk_work_contracts_active_per_workplace
    ON work_contracts (workplace_id)
    WHERE effective_to IS NULL;

CREATE INDEX idx_work_contracts_workplace_effective_from
    ON work_contracts (workplace_id, effective_from DESC, id DESC);

ALTER TABLE work_contracts
    ADD CONSTRAINT ex_work_contracts_no_overlap
    EXCLUDE USING gist (
        workplace_id WITH =,
        daterange(effective_from, COALESCE(effective_to + 1, 'infinity'::date), '[)') WITH &&
    );

CREATE TABLE work_proofs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES users (id),
    workplace_id BIGINT REFERENCES workplaces (id),
    contract_id BIGINT REFERENCES work_contracts (id),
    work_date DATE NOT NULL,
    clock_in_at TIMESTAMP NOT NULL,
    clock_out_at TIMESTAMP,
    device_clock_in_at TIMESTAMP NOT NULL,
    device_clock_out_at TIMESTAMP,
    server_clock_in_at TIMESTAMP NOT NULL,
    server_clock_out_at TIMESTAMP,
    clock_in_latitude DOUBLE PRECISION NOT NULL,
    clock_in_longitude DOUBLE PRECISION NOT NULL,
    clock_out_latitude DOUBLE PRECISION,
    clock_out_longitude DOUBLE PRECISION,
    clock_in_location_label VARCHAR(100),
    clock_out_location_label VARCHAR(100),
    clock_out_outside_allowed_radius BOOLEAN,
    memo VARCHAR(500),
    edit_reason VARCHAR(500),
    attachment_count INTEGER NOT NULL DEFAULT 0,
    attachment_metadata_json TEXT,
    financial_status VARCHAR(20) NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT ck_work_proofs_work_date CHECK (
        work_date = clock_in_at::date
    ),
    CONSTRAINT ck_work_proofs_contract_pair CHECK (
        (workplace_id IS NULL AND contract_id IS NULL)
        OR (workplace_id IS NOT NULL AND contract_id IS NOT NULL)
    ),
    CONSTRAINT ck_work_proofs_clock_out_order CHECK (
        clock_out_at IS NULL OR clock_out_at > clock_in_at
    ),
    CONSTRAINT ck_work_proofs_device_clock_out_order CHECK (
        device_clock_out_at IS NULL OR device_clock_out_at > device_clock_in_at
    ),
    CONSTRAINT ck_work_proofs_server_clock_out_order CHECK (
        server_clock_out_at IS NULL OR server_clock_out_at >= server_clock_in_at
    ),
    CONSTRAINT ck_work_proofs_clock_out_payload CHECK (
        (
            clock_out_at IS NULL
            AND device_clock_out_at IS NULL
            AND server_clock_out_at IS NULL
            AND clock_out_latitude IS NULL
            AND clock_out_longitude IS NULL
        )
        OR (
            clock_out_at IS NOT NULL
            AND device_clock_out_at IS NOT NULL
            AND server_clock_out_at IS NOT NULL
            AND clock_out_latitude IS NOT NULL
            AND clock_out_longitude IS NOT NULL
        )
    ),
    CONSTRAINT ck_work_proofs_checkout_review_payload CHECK (
        clock_out_outside_allowed_radius IS NULL OR clock_out_at IS NOT NULL
    ),
    CONSTRAINT ck_work_proofs_attachment_count CHECK (attachment_count >= 0),
    CONSTRAINT ck_work_proofs_attachment_metadata_json CHECK (
        attachment_metadata_json IS NULL
        OR jsonb_typeof(attachment_metadata_json::jsonb) = 'array'
    ),
    CONSTRAINT ck_work_proofs_financial_status CHECK (
        financial_status IN ('PENDING', 'NEEDS_REVIEW', 'REFLECTED')
    ),
    CONSTRAINT ck_work_proofs_financial_status_payload CHECK (
        (financial_status = 'PENDING' AND clock_out_at IS NULL)
        OR (financial_status = 'REFLECTED' AND clock_out_at IS NOT NULL)
        OR (
            financial_status = 'NEEDS_REVIEW'
            AND clock_out_at IS NOT NULL
            AND clock_out_outside_allowed_radius IS TRUE
        )
    ),
    CONSTRAINT ck_work_proofs_clock_in_latitude CHECK (
        clock_in_latitude BETWEEN -90 AND 90
    ),
    CONSTRAINT ck_work_proofs_clock_in_longitude CHECK (
        clock_in_longitude BETWEEN -180 AND 180
    ),
    CONSTRAINT ck_work_proofs_clock_out_latitude CHECK (
        clock_out_latitude IS NULL OR clock_out_latitude BETWEEN -90 AND 90
    ),
    CONSTRAINT ck_work_proofs_clock_out_longitude CHECK (
        clock_out_longitude IS NULL OR clock_out_longitude BETWEEN -180 AND 180
    )
);

CREATE INDEX idx_work_proofs_user_work_date_clock_in
    ON work_proofs (user_id, work_date DESC, clock_in_at DESC, id DESC);

CREATE INDEX idx_work_proofs_user_workplace_work_date_clock_in
    ON work_proofs (user_id, workplace_id, work_date DESC, clock_in_at DESC, id DESC)
    WHERE workplace_id IS NOT NULL;

CREATE INDEX idx_work_proofs_user_open_created_at
    ON work_proofs (user_id, created_at DESC, id DESC)
    WHERE clock_out_at IS NULL;

