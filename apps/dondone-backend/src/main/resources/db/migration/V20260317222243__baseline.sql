CREATE TABLE advance_requests (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES users (id),
    workplace_id BIGINT NOT NULL REFERENCES workplaces (id),
    contract_id BIGINT NOT NULL REFERENCES work_contracts (id),
    year_month VARCHAR(7) NOT NULL,
    idempotency_key VARCHAR(120) NOT NULL,
    requested_amount BIGINT NOT NULL,
    approved_amount BIGINT NOT NULL,
    fee_amount BIGINT NOT NULL,
    status VARCHAR(20) NOT NULL,
    repayment_due_date DATE NOT NULL,
    requested_at TIMESTAMP NOT NULL,
    snapshot_available_amount BIGINT NOT NULL,
    snapshot_max_cap BIGINT NOT NULL,
    snapshot_policy_rate NUMERIC(5, 2) NOT NULL,
    snapshot_reflected_work_days INTEGER NOT NULL,
    snapshot_reflected_work_minutes BIGINT NOT NULL,
    snapshot_needs_review_record_count INTEGER NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uk_advance_requests_user_idempotency UNIQUE (user_id, idempotency_key),
    CONSTRAINT ck_advance_requests_year_month CHECK (
        year_month ~ '^\d{4}-(0[1-9]|1[0-2])$'
    ),
    CONSTRAINT ck_advance_requests_status CHECK (
        status IN ('SUBMITTED', 'APPROVED', 'REJECTED', 'NEEDS_REVIEW')
    ),
    CONSTRAINT ck_advance_requests_amounts CHECK (
        requested_amount > 0
        AND approved_amount >= 0
        AND approved_amount <= requested_amount
        AND fee_amount >= 0
        AND snapshot_available_amount >= 0
        AND snapshot_max_cap >= 0
    ),
    CONSTRAINT ck_advance_requests_snapshot_policy_rate CHECK (
        snapshot_policy_rate >= 0 AND snapshot_policy_rate <= 1
    ),
    CONSTRAINT ck_advance_requests_snapshot_counts CHECK (
        snapshot_reflected_work_days >= 0
        AND snapshot_reflected_work_minutes >= 0
        AND snapshot_needs_review_record_count >= 0
    ),
    CONSTRAINT ck_advance_requests_repayment_due_date CHECK (
        repayment_due_date >= requested_at::date
    )
);

CREATE UNIQUE INDEX uk_advance_requests_approved_month
    ON advance_requests (user_id, workplace_id, year_month)
    WHERE status = 'APPROVED';

CREATE INDEX idx_advance_requests_user_year_month_requested_at
    ON advance_requests (user_id, year_month, requested_at DESC, created_at DESC, id DESC);

